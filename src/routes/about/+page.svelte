<h1 id="big-header">What is <span>Hatch</span>?</h1>
<p>
  Hatch is a third-party project hosting service for projects made using Scratch and TurboWarp.
  Scratch is a great option to learn the fundamentals of programming, but it is designed for young
  children. Many Scratchers understandably find themselves outgrowing Scratch as they get older,
  and/or have distaste for new Scratch features, but they want to keep using the Scratch editor to
  create projects.
</p>
<p>
  Hatch was designed with this group of users in mind. While we're very similar to Scratch, having a
  close online community, we have more relaxed rules, allowing for more content types and genres
  that may not be suitable for the Scratch website.
</p>
<h2>Frequently Asked Questions</h2>
<h3>Who can join Hatch?</h3>
<p>
  We allow anyone who is aged 13 or older to join. If you are under 13, you are welcome to use Hatch
  while logged out and access any projects that aren't age restricted.
</p>
<h3>Why should I use Hatch?</h3>
<p>
  There are other good third-party options for hosting Scratch projects. However, Hatch is far less
  limited. We are very transparent between the Hatch Team and Hatchlings (users), and our rules are
  much more relaxed. We also contain features that some other similar sites may not, such as
  commenting.
</p>
<h3>Can I advertise Hatch or my Hatch Projects on Scratch?</h3>
<p>
  No. Scratch has rules that prohibit sharing links to websites with looser moderation guidelines,
  because Scratch is a platform for users of all ages.
  <strong
    >Please do not share links to Hatch or mention Hatch anywhere on the Scratch website.</strong
  >
</p>
